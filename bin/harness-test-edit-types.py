def run_test(test: dict) -> dict:
    """Run a single test case and return the result record."""
    context_files = test.get("context_files", {})
    recent_diffs = test.get("recent_diffs", [])
    prompt = build_prompt(
        context_files,
        recent_diffs,
        test["file_path"],
        test["original_content"],
        test["current_content"],
    )

    start = time.time()
    try:
        resp = completion_request(prompt)
        content = resp["content"]
        error = None
    except Exception as e:
        content = ""
        error = str(e)
    elapsed = time.time() - start

    return {
        "language": test["language"],
        "mode": test["mode"],
        "expected_edit_kind": test["expected_edit_kind"],
        "label": test["label"],
        "file_path": test["file_path"],
        "cursor_line": test.get("cursor_line", 0),
        "original_content": test["original_content"],
        "current_content": test["current_content"],
        "prompt": prompt,
        "response": content,
        "error": error,
        "elapsed_seconds": round(elapsed, 3),
    }


def main() -> int:
    # Health check
    try:
        httpx.get(f"{BASE_URL}/health", timeout=5.0).raise_for_status()
    except (httpx.ConnectError, httpx.HTTPStatusError) as e:
        print(f"Error: cannot reach llama-server at {BASE_URL}: {e}", file=sys.stderr)
        print("Start it first with: ./07-harness-sweepai-llama-cpp-server.sh", file=sys.stderr)
        return 1

    print(f"Server reachable at {BASE_URL}")
    print(f"Running {len(TESTS)} tests across 10 languages ...\n")

    results = []
    for i, test in enumerate(TESTS, 1):
        label = test["label"]
        print(f"[{i}/{len(TESTS)}] {label} ... ", end="", flush=True)
        record = run_test(test)
        results.append(record)
        if record["error"]:
            print(f"ERROR ({record['elapsed_seconds']}s): {record['error']}")
        else:
            resp_preview = record["response"][:80].replace("\n", "\\n")
            print(f"OK ({record['elapsed_seconds']}s) -> {resp_preview}...")

    # Write output
    with open(OUTPUT_FILE, "w") as f:
        for record in results:
            f.write(json.dumps(record, ensure_ascii=False) + "\n")

    print(f"\n{'=' * 60}")
    print(f"Done. {len(results)} results written to {OUTPUT_FILE}")
    print(f"{'=' * 60}")

    errors = [r for r in results if r["error"]]
    if errors:
        print(f"\n{len(errors)} test(s) had errors.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
